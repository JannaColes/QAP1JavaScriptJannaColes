// The chalk module is a great example of a third-party module. It is used to print strings to the console in color.

// Import the chalk module
const chalk = require('chalk');

// Use the chalk module to print a string to the console in color
console.log(chalk.blue('Hello, this is my first Node app! In blue!'));
console.log(chalk.red('Hello, this is my first Node app! In red!'));
console.log(chalk.green('Hello, this is my first Node app! In green!'));